// Bee
//
// A class to define how the bees are made and behave on the screen.
//

// Bee constructor
//
// Sets the properties with the provided arguments
function Bee(x,y,vx,vy,size,speed,img) {
  this.x = x;
  this.y = y;
  this.vx = vx;
  this.vy = vy;
  this.size = size;
  this.speed = speed;
  this.img = img;
}
